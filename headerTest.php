<h2>Faculty fulfilling their promise</h2>
<p class="meta">campus success</p>
<p>In addition to being ranked a tier-one school in the <em>U.S. News &amp; World Report</em> rankings of national universities, IUPUI has been ranked 16th in a new measure of institutions’ commitment to undergraduate teaching. The ranking included IUPUI among several institutions where the faculty has an unusually strong commitment to undergraduate teaching, instead of focusing solely on graduate-level research.</p>
<a class="button" href="http://news.iupui.edu/releases/iu/2015/09/us-news-and-forbes-rankings.shtml">Read about the national rankings</a>
</div>
</div>
<div class="feature">
<figure class="media image"><img alt="IUPUI student Amy Mattingly" src="http://www.iupui.edu/images/home/spotlights/spotlight-forall-mattingly.jpg"></figure><div class="content">
<h2>A career that comes full circle</h2>
<p class="meta">student success</p>
<p>In 1993, 6-month-old Amy Mattingly required life-saving surgery to remove a large tumor from her abdomen. As an IUPUI student, athlete, and scholarship recipient, she majored in radiation therapy so that she could pursue a career helping others in similarly critical conditions. The IU Foundation made her dream possible by funding her scholarship. Now we need your help so students like Amy achieve more than ever before as part of <em>For All: The Indiana University Bicentennial Campaign</em>.</p>
<a class="button" href="https://iufoundation.iu.edu/campaign/student-success.html">Join us</a>
</div>
</div>
<div class="feature">
<figure class="media image"><img alt="IUPUI student Cherry Kasipathi paints a wall" src="http://www.iupui.edu/images/home/spotlights/spotlight-day-caring.jpg"></figure><div class="content">
<h2>Lending a hand</h2>
<p class="meta">campus success</p>
<p>On October 2, nearly 400 IUPUI students assisted Indianapolis community organizations with cleaning, painting, landscaping, and making a difference as part of the United Way Day of Caring. Among the organizations that received help from students included the Baxter YMCA, MLK Park, White River State Park, Vonnegut Library, and the Indiana State Museum.</p>
<a class="button" href="http://inside.iupui.edu/features/photos/2015-10-06-day-of-caring.shtml">See photos from the Day of Caring</a>
